package model;

import java.sql.Time;

/**
 *
 * @author KakaNoob
 */
public class Slot {
    private int slotNo;
    private Time start;
    private Time end;

    public Slot() {
    }

    public int getSlotNo() {
        return slotNo;
    }

    public void setSlotNo(int slotNo) {
        this.slotNo = slotNo;
    }

    public Time getStart() {
        return start;
    }

    public void setStart(Time start) {
        this.start = start;
    }

    public Time getEnd() {
        return end;
    }

    public void setEnd(Time end) {
        this.end = end;
    }
    
}
